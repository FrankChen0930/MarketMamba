# 建立反向查詢表：stock_id → sector
STOCK_TO_SECTOR = {}
for sector, stocks in SECTOR_GROUPS.items():
    for stock_id in stocks:
        STOCK_TO_SECTOR[stock_id] = sector


def get_sector(stock_id: str) -> str:
    """查詢股票所屬產業"""
    return STOCK_TO_SECTOR.get(str(stock_id).strip(), "未分類")


def get_sector_peers(stock_id: str) -> list[str]:
    """取得同產業的所有股票"""
    sector = get_sector(stock_id)
    if sector == "未分類":
        return []
    return [s for s in SECTOR_GROUPS.get(sector, []) if s != stock_id]


def get_all_sectors() -> list[str]:
    """取得所有產業分類名稱"""
    return list(SECTOR_GROUPS.keys())
